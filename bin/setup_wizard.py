"""First-run reviewer.toml provisioning. Stdlib only.

Auto-creates `~/.spectre/reviewer.toml` on first /vision invocation so the user
never needs to drop the config from a side-shell. Four outcomes:

  - exists   — config already present; no-op
  - enabled  — key detected, user said yes, wrote enabled=true
  - declined — key detected, user said no, wrote enabled=false (placeholder
               so we don't re-prompt next run)
  - no-key   — no DeepSeek key found anywhere; wrote enabled=false placeholder

The placeholder behavior is load-bearing: writing `enabled=false` after a "no"
or "no-key" outcome means /vision will surface a `tier3-skipped` finding on
every subsequent run rather than re-running this wizard. Re-enable by editing
the TOML or deleting it.

Key discovery order:
  1. Live environment variable (default name `DEEPSEEK_API_KEY`)
  2. Any `.env`-style file passed as `secrets_file_path` (KEY=value lines)

The secrets file is host-specific and not hardcoded — callers pass the path
they trust. /vision passes None by default; users with a custom secrets store
can wire it up via the `SPECTRE_SECRETS_FILE` env var (see §6.3a in vision/SKILL.md).

Public API:
    config_path_default() -> pathlib.Path
    detect_api_key(*, env_var_name, secrets_file_path) -> tuple[str, str] | None
    write_config(path, *, enabled, api_key_env) -> None
    maybe_provision(target, *, secrets_file_path, prompt_fn) -> str
"""
from __future__ import annotations

import os
import pathlib
import sys
import tempfile


def config_path_default() -> pathlib.Path:
    """Return the canonical reviewer.toml location."""
    return pathlib.Path.home() / ".spectre" / "reviewer.toml"


def secrets_path_default() -> pathlib.Path:
    """Return the canonical secrets.env location.

    v0.3.2: ~/.spectre/secrets.env is the single, in-Spectre place for the
    DeepSeek API key. No host-specific paths, no env-var rituals.
    """
    return pathlib.Path.home() / ".spectre" / "secrets.env"


def _resolve_secrets_file_path(explicit: pathlib.Path | None) -> pathlib.Path:
    """Resolve which secrets file to probe.

    Priority: explicit argument > SPECTRE_SECRETS_FILE env var > ~/.spectre/secrets.env.
    The default is the canonical Spectre-owned location — no host-specific paths,
    no env-var rituals required for first-time users.
    """
    if explicit is not None:
        return explicit
    env_path = os.environ.get("SPECTRE_SECRETS_FILE")
    if env_path:
        return pathlib.Path(env_path)
    return secrets_path_default()


def detect_api_key(
    *,
    env_var_name: str = "DEEPSEEK_API_KEY",
    secrets_file_path: pathlib.Path | None = None,
) -> tuple[str, str] | None:
    """Look for a DeepSeek API key.

    Order: live env var first, then read-only check of an optional `.env`-style
    secrets file (KEY=value lines). The secrets file path is supplied by the
    caller (or via SPECTRE_SECRETS_FILE env var) — never hardcoded. Never reads
    the value — only confirms presence.

    Returns (source, location) where source is "env" or "secrets-file", or None.
    """
    if os.environ.get(env_var_name):
        return ("env", env_var_name)
    secrets_path = _resolve_secrets_file_path(secrets_file_path)
    if secrets_path.is_file():
        try:
            content = secrets_path.read_text(encoding="utf-8")
        except (OSError, PermissionError):
            return None
        for line in content.splitlines():
            stripped = line.strip()
            if stripped.startswith(f"{env_var_name}="):
                return ("secrets-file", str(secrets_path))
    return None


def write_config(
    path: pathlib.Path,
    *,
    enabled: bool,
    api_key_env: str,
    model: str = "deepseek-reasoner",
    base_url: str = "https://api.deepseek.com/v1",
    budget_tokens_per_spec: int = 50_000,
    chunk_timeout_s: int = 60,
    total_timeout_s: int = 600,
    timeout_s: int | None = None,
) -> None:
    """Atomically write reviewer.toml at mode 0600.

    Writes both chunk_timeout_s (per-recv socket timeout) and total_timeout_s
    (hard wall-clock ceiling). The legacy timeout_s parameter is accepted for
    back-compat only; when provided it overrides chunk_timeout_s.
    """
    if timeout_s is not None:
        chunk_timeout_s = timeout_s
    path.parent.mkdir(parents=True, exist_ok=True)
    enabled_str = "true" if enabled else "false"
    body = (
        f"# Spectre reviewer config — auto-generated by setup_wizard.\n"
        f"# Edit `enabled` to toggle Tier 3 adversarial review.\n"
        f"\n"
        f"[tier3]\n"
        f"enabled = {enabled_str}\n"
        f'api_key_env = "{api_key_env}"\n'
        f'model = "{model}"\n'
        f'base_url = "{base_url}"\n'
        f"budget_tokens_per_spec = {budget_tokens_per_spec}\n"
        f"chunk_timeout_s = {chunk_timeout_s}\n"
        f"total_timeout_s = {total_timeout_s}\n"
    )
    fd, tmp_path = tempfile.mkstemp(
        prefix=path.name + ".", dir=str(path.parent), suffix=".tmp"
    )
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as f:
            f.write(body)
        os.chmod(tmp_path, 0o600)
        os.replace(tmp_path, path)
    except Exception:
        try:
            os.unlink(tmp_path)
        except OSError:
            pass
        raise


def maybe_provision(
    target: pathlib.Path,
    *,
    secrets_file_path: pathlib.Path | None = None,
    api_key_env: str = "DEEPSEEK_API_KEY",
) -> str:
    """Provision reviewer.toml if missing.

    Outcomes:
      - "exists"        — config already present, no-op.
      - "enabled"       — key found; Tier 3 enabled silently.
      - "setup-skipped" — no key found; enabled=false placeholder written silently.

    Both decisions are silent and non-interactive. Configuring the API key in
    ~/.spectre/secrets.env (or DEEPSEEK_API_KEY env var) is itself the opt-in
    signal — no in-flow prompt is fired. This keeps /vision safe for
    non-interactive contexts (subagents, scripts, paste-stdin, observer flows)
    where input() raises EOFError.

    To opt out after enabling: edit ~/.spectre/reviewer.toml and set
    [tier3] enabled = false (the file persists across runs once written).
    To re-prompt provisioning: delete ~/.spectre/reviewer.toml.
    """
    if target.exists():
        return "exists"

    detection = detect_api_key(env_var_name=api_key_env, secrets_file_path=secrets_file_path)
    if detection is not None:
        write_config(target, enabled=True, api_key_env=api_key_env)
        return "enabled"

    # No key. Silent skip + visible placeholder + stderr breadcrumb.
    write_config(target, enabled=False, api_key_env=api_key_env)
    secrets_path = _resolve_secrets_file_path(secrets_file_path)
    print(
        f"Tier 3 adversarial review skipped — no {api_key_env} found. "
        f"Configure {secrets_path} (mode 0600) and re-run /vision to enable.",
        file=sys.stderr,
    )
    return "setup-skipped"


def walker_path_default() -> pathlib.Path:
    """Return the canonical walker.toml location."""
